/*
* Para a PROGRAMAÇÃO ESTRUTURADA, Chamamos de FUNÇÔES. AQUI EM POO, CHAMAMOS DE MÉTODO!
* 
* 
* 
* “Funções” e “procedimentos” são termos próprios da programação estruturada (PE). 
* Na programação orientada a objetos (POO), o termo geral é “método”. 
* 
* A declaração de um método segue esta sintaxe:
* ==============================================================================================
* [<modificador de acesso>] <tipo de retorno> <nome> (<parametros>)
* {
*   código
* }
* ==============================================================================================
*
*==================================================================================== 
*  1 - O modificador de acesso pode ser public, private, protected, entre outros. 
*  2 - O tipo de retorno indica que tipo de informação o método pode devolver ao código que o chamou.
*  3 - “Nome” é como o método será chamado em nosso código.
*  4 - Parâmetros são as informações de entrada que o método pode usar em seu processamento. 
*  5 - Código é uma sequência de comandos que pode incluir desvios, laços, criação de variáveis etc. 
